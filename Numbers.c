#include<stdio.h>
#include<math.h> // code ran correctly without this but there was an error as well so probably best to include this

void main() {
    printf("%f\n", 5 + 4.5); // %f is for floating numbers i.e 1.5, if one number isn't a floating number the answer will return as 0.000000

    int num = 6;
    printf("%d\n", num);

    printf("%f\n", pow(2, 3)); // prints 2 to the power of 3

    printf("%f\n", sqrt(36)); // print the square root of 36

    printf("%f\n", ceil(36.356)); // prints next whole number
}
